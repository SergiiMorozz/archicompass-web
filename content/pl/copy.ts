export type ArticleSlug = keyof typeof articleCopyBySlug;

export function applyPolishArticleCopy<T extends { slug: string }>(article: T): T {
  const copy = articleCopyBySlug[article.slug as ArticleSlug];
  return copy
    ? ({
        ...article,
        author_name: copy.author,
        body: copy.body.join("\n\n"),
        category: copy.category,
        excerpt: copy.excerpt,
        published_at: copy.date,
        title: copy.title,
      } as T)
    : article;
}

